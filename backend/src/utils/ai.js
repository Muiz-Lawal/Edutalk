export const moderateContent = async (content) => {
  try {
    if (!OPENAI_API_KEY) {
      return { isSafe: true, flaggedCategories: [] };
    }

    const response = await axios.post(
      `${OPENAI_API_URL}/moderations`,
      {
        input: content,
      },
      {
        headers: {
          Authorization: `Bearer ${OPENAI_API_KEY}`,
          'Content-Type': 'application/json',
        },
      }
    );

    const result = response.data.results[0];

    return {
      isSafe: !result.flagged,
      flaggedCategories: Object.keys(result.categories).filter(cat => result.categories[cat]),
      scores: result.category_scores,
    };
  } catch (error) {
    console.error('Error moderating content:', error);
    return { isSafe: true, flaggedCategories: [] };
  }
};


export const detectLanguage = async (text) => {
  try {
    // This is a simple language detection
    // For production, use a dedicated library like 'detect-language' or OpenAI
    const languages = {
      english: /hello|world|good|morning/i,
      spanish: /hola|buenos|días/i,
      french: /bonjour|salut/i,
      chinese: /你好|早上好/,
    };

    for (const [lang, pattern] of Object.entries(languages)) {
      if (pattern.test(text)) {
        return lang;
      }
    }

    return 'english'; // Default
  } catch (error) {
    console.error('Error detecting language:', error);
    return 'english';
  }
};

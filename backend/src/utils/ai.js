export const summarizeContent = async (transcript) => {
  try {
    if (!OPENAI_API_KEY) {
      console.warn('OpenAI API key not configured. Using mock summary.');
      return 'This session covered the main concepts and provided practical examples.';
    }

    // Call OpenAI API for summarization
    const response = await axios.post(
      `${OPENAI_API_URL}/chat/completions`,
      {
        model: 'gpt-4',
        messages: [
          {
            role: 'system',
            content: 'You are an expert at summarizing educational content. Provide a concise 2-3 sentence summary.',
          },
          {
            role: 'user',
            content: `Summarize this lecture transcript:\n\n${transcript}`,
          },
        ],
        temperature: 0.7,
        max_tokens: 150,
      },
      {
        headers: {
          Authorization: `Bearer ${OPENAI_API_KEY}`,
          'Content-Type': 'application/json',
        },
      }
    );

    return response.data.choices[0].message.content;
  } catch (error) {
    console.error('Error summarizing content:', error);
    return 'Unable to generate summary at this time.';
  }
};

